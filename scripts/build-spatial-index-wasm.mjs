import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import wabt from 'wabt';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const watPath = path.join(repoRoot, 'src', 'engine', 'wasm', 'spatialIndexKernel.wat');
const outPath = path.join(repoRoot, 'src', 'engine', 'wasm', 'generated', 'spatialIndexKernel.ts');

const source = await fs.readFile(watPath, 'utf8');
const wabtApi = await wabt();
const module = wabtApi.parseWat(watPath, source);
const { buffer } = module.toBinary({ log: false, write_debug_names: false });

const lines = [];
for (let index = 0; index < buffer.length; index += 24) {
  lines.push(`  ${Array.from(buffer.slice(index, index + 24)).join(', ')}`);
}

const content = `// Generated by scripts/build-spatial-index-wasm.mjs. Do not edit by hand.\nexport const SPATIAL_INDEX_WASM_BYTES = new Uint8Array([\n${lines.join(',\n')}\n]);\n`;
await fs.writeFile(outPath, content, 'utf8');
console.log(`Generated ${path.relative(repoRoot, outPath)} (${buffer.length} bytes)`);